import { useUser } from '@auth0/nextjs-auth0'
import Head from 'next/head'
import Layout from '../components/Layout'
import Image from 'next/image';
import { useRouter } from 'next/router';
import Trakt_Button from '../components/profile/Trakt_Button';
import { useEffect } from 'react';
import { useState } from 'react';

export default function Profile() {
  const user = useUser();
  const [watched, setWatched] = useState({ movies: null, shows: null });

  useEffect(() => {
    async function getWatched() {
      if (!user.isLoading && user.user) {
        var userInfo = await fetch(`/api/user/email/'${user.user.email}'`);
        userInfo = await userInfo.json();

        var body = {
          user: userInfo.res,
          movie: true
        }

        const options = {
          method: 'POST',
          headers: {
              'Content-Type': 'application/json',
          },
          body: JSON.stringify(body)
        };

        var response = await fetch('/api/user/get-history', options);
        response = await response.json();
        setWatched({ movies: response })
      }
    }

    getWatched();
  }, [user.isLoading])

  if (user.isLoading) {
    return (
      <h1>Loading!</h1>
    )
  }
  if (!user.user) {
    window.location.href = '/landing';
  }

  console.log(watched);

  return (
    <Layout>
      <div>
        <Head>
          <title>Mimir</title>
          <meta name="description" content="Generated by create next app" />
          <link rel="icon" href="/favicon.ico" />
        </Head>

        <main className='flex flex-col items-center'>
          <div className='flex flex-row justify-center space-x-4'>
            <div className='w-32 h-32 relative'>
              <Image
                src={user.user.picture}
                alt={user.user.name}
                layout='fill'
                quality={100}
                priority
                className='w-32 h-32 rounded-full'
              />
            </div>
            <div className='flex items-center'>
              <h1 className='text-3xl'>{user.user.nickname}</h1>
            </div>
          </div>

          <Trakt_Button />

        </main>
      </div>
    </Layout>
  )
}